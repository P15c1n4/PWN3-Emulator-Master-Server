package Model;

public class Conta {
    private String accId;
    private String accName;
    private String accPass;
    private String accTeamName;
    private String accTeamHash;

    /**
     * @return the accId
     */
    public String getAccId() {
        return accId;
    }

    /**
     * @param accId the accId to set
     */
    public void setAccId(String accId) {
        this.accId = accId;
    }

    /**
     * @return the accName
     */
    public String getAccName() {
        return accName;
    }

    /**
     * @param accName the accName to set
     */
    public void setAccName(String accName) {
        this.accName = accName;
    }

    /**
     * @return the accPass
     */
    public String getAccPass() {
        return accPass;
    }

    /**
     * @param accPass the accPass to set
     */
    public void setAccPass(String accPass) {
        this.accPass = accPass;
    }

    /**
     * @return the accTeamName
     */
    public String getAccTeamName() {
        return accTeamName;
    }

    /**
     * @param accTeamName the accTeamName to set
     */
    public void setAccTeamName(String accTeamName) {
        this.accTeamName = accTeamName;
    }

    /**
     * @return the accTeamHash
     */
    public String getAccTeamHash() {
        return accTeamHash;
    }

    /**
     * @param accTeamHash the accTeamHash to set
     */
    public void setAccTeamHash(String accTeamHash) {
        this.accTeamHash = accTeamHash;
    }
    

    
}
