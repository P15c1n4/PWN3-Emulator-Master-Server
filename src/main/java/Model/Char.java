package Model;

public class Char extends Conta{
    private String charName;
    private String charId;
    private String charSpec;
    private String charStatus;
    private String charLocal;

    /**
     * @return the charName
     */
    public String getCharName() {
        return charName;
    }

    /**
     * @param charName the charName to set
     */
    public void setCharName(String charName) {
        this.charName = charName;
    }

    /**
     * @return the charId
     */
    public String getCharId() {
        return charId;
    }

    /**
     * @param charId the charId to set
     */
    public void setCharId(String charId) {
        this.charId = charId;
    }

    /**
     * @return the charSpec
     */
    public String getCharSpec() {
        return charSpec;
    }

    /**
     * @param charSpec the charSpec to set
     */
    public void setCharSpec(String charSpec) {
        this.charSpec = charSpec;
    }

    /**
     * @return the charStatus
     */
    public String getCharStatus() {
        return charStatus;
    }

    /**
     * @param charStatus the charStatus to set
     */
    public void setCharStatus(String charStatus) {
        this.charStatus = charStatus;
    }

    /**
     * @return the charLocal
     */
    public String getCharLocal() {
        return charLocal;
    }

    /**
     * @param charLocal the charLocal to set
     */
    public void setCharLocal(String charLocal) {
        this.charLocal = charLocal;
    }
    
    
    
}
